# booleans are binary types: they are either just True or False
my_bool_1 = True
my_bool_2 = False

print("Bool 1 :",my_bool_1)
print("Bool 2 :",my_bool_2)

# Booleans are really useful on testing stuff
print("")
print(2 == 3)
print(2 > 3)
print(2 < 3)
print(2 >= 3)
print(2 <= 3)
print(2 != 3)

# And there are logical operators
print(True and True)
print(True and False)
print(True or True)
print(True or False)
print(not(True))

